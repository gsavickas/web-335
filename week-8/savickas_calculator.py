# This is the addition function. It takes 2 numbers and adds them. It then returns the output.
def add(item1, item2):
  return(item1 + item2)

# This is the subtraction function. It takes 2 numbers and subtracts them. It then returns the output.
def subtract(item1, item2):
  return(item1 - item2)

# This is the divide function. It takes 2 numbers and divides the first by the second. It then returns the output.
def divide(item1, item2):
  return(item1 / item2)

print(add(5,5))

print(subtract(6,3))

print(divide(6,3))
